//OPERACIONES MATEMÁTICAS


/// @fn int Suma(int, int, int)
/// @brief Suma dos operandos
///
/// @param operandoUno
/// @param operandoDos
/// @param resultadoSuma
/// @return Retorna el resultado de la operación
int Suma(int operandoUno, int operandoDos, int resultadoSuma);

/// @fn int Resta(int, int, int)
/// @brief Resta dos operandos
///
/// @param operandoUno
/// @param operandoDos
/// @param resultadoResta
/// @return Retorna el resultado de la operación
int Resta(int operandoUno, int operandoDos, int resultadoResta);

/// @fn float Division(int, int, float)
/// @brief Divide dos operandos
///
/// @param operandoUno
/// @param operandoDos
/// @param resultadoDivision
/// @return Retorna el resultado de la operación
float Division(int operandoUno, int operandoDos, float resultadoDivision);

/// @fn int Multiplicacion(int, int, int)
/// @brief Multiplica dos operandos
///
/// @param operandoUno
/// @param operandoDos
/// @param resultadoMult
/// @return Retorna el resultado de la operación
int Multiplicacion(int operandoUno, int operandoDos, int resultadoMult);

/// @fn int FactorialUno(int, int)
/// @brief Realiza el factorial de un número
///
/// @param operando
/// @param resultadoFactorialA
/// @return Retorna el resultado de la operación
int Factorial(int operando, int resultadoFactorialA);
